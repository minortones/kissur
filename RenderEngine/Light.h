#ifndef LIGHT_H
#define LIGHT_H

#include "Structs.h"

typedef struct Light
{
	VECTOR3 position;
	VECTOR3 color;

	static inline Light create( const VECTOR3& pPos, const VECTOR3& pCol )
	{
		Light l	= { pPos, pCol };
		return l;
	}

}Light;

#endif